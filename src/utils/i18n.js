// Aliases for missing languages to fallback dynamically
const ALIASES = {
  Svenska: "Swedish",
  Norsk: "Norwegian",
  Dansk: "Danish",
  Polski: "Polish",
  Čeština: "Czech",
  Magyar: "Hungarian",
  Română: "Romanian",
  Türkçe: "Turkish",
  Русский: "Russian",
  Українська: "Ukrainian",
  Arabic: "Arabic"
};

export function getNormalizedLanguage(lang) {
  if (!lang) return 'English';
  // Check exact match
  if (TRANSLATIONS[lang]) return lang;
  // Check aliases
  if (ALIASES[lang]) return ALIASES[lang];
  // Match by normalized language names
  if (lang === 'Bengali' || lang === 'বাংলা' || lang === 'Bengali (বাংলা)') return 'বাংলা';
  if (lang === 'German' || lang === 'Deutsch') return 'Deutsch';
  if (lang === 'French' || lang === 'Français') return 'Français';
  if (lang === 'Spanish' || lang === 'Español') return 'Español';
  if (lang === 'Italian' || lang === 'Italiano') return 'Italiano';
  if (lang === 'Portuguese' || lang === 'Português') return 'Português';
  if (lang === 'Dutch' || lang === 'Nederlands') return 'Nederlands';
  if (lang === 'Hindi' || lang === 'हिन्दी') return 'हिन्दी';
  if (lang === 'Chinese (Simplified)' || lang === '简体中文') return '简体中文';
  if (lang === 'Japanese' || lang === '日本語') return '日本語';
  return 'English';
}

export function useTranslation() {
  const storeLanguage = useFinanceStore(state => state.language) || 'English';
  const language = getNormalizedLanguage(storeLanguage);
  
  const t = (key, fallback = '') => {
    const dict = TRANSLATIONS[language] || TRANSLATIONS.English;
    return dict[key] || TRANSLATIONS.English[key] || fallback || key;
  };
  
  return { t, language };
}
